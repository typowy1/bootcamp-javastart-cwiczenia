package cwiczenia.lekcja14.zadanie8_mapafilmowzpliku;

public class TitleAndYear {

    private final String title;
    private final int year;

    public TitleAndYear(String title, int year) {
        this.title = title;
        this.year = year;
    }

    public String getTitle() {
        return title;
    }

    public int getYear() {
        return year;
    }
}
