//    PrintWriter - klasa, która jest podobna do PrintStream (czyli ta, którą wykorzystujemy do System.out.print()) -
//    pozwala na prosty zapis danych do pliku tekstowego. W odróżnieniu od BufferedWritera nie wykorzystuje bufora,
//    więc raczej powinno się ją wykorzystywać do zapisu niewielkiej ilości danych,
//    Scanner - można go wykorzystać do odczytu plików podobnie jak do odczytu danych z konsoli.

